public class Main {
    /**
     * @param args
     */
    public static void main(String[] args) {
        // 1. Max/Min
        System.out.println(Math.max(10, 20));

        // 2. Sqrt
        System.out.println(Math.sqrt(20));

        // 3. Abs
        System.out.println(Math.abs(-2));

        // 4. Random no
        System.out.println((int) (Math.random() * 101));
    }
}
